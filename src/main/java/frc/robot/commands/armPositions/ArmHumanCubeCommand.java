package frc.robot.commands.armPositions;
import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.Arm;

public class ArmHumanCubeCommand extends CommandBase{
    Arm arm;

    public ArmHumanCubeCommand(Arm arm){
        addRequirements(arm);
        this.arm = arm;
    }

    public void execute(){
        arm.armHumanCube();
    }

} // <-- keep brace 
